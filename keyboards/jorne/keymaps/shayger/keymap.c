bool process_record_user(uint16_t keycode, keyrecord_t* record) {
    switch (keycode) {
        // Luna jumping on space
        case KC_SPC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;
            return false;

        case KC_LGUI:
            if (record->event.pressed) {
                isBarking  = true;
            } else {
                isBarking = false;
            }
            break;
            return false;

        case KC_LALT:

            if (record->event.pressed) {
                isSneaking  = true;
            } else {
                isSneaking = false;
            }
            break;
            return false;
    }
    return true;
}
